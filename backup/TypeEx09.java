package backup;
public class TypeEx09 {
    public static void main(String[]args){

        //변수
        int vNum = 10;
        vNum = 20;
        System.out.println(vNum);

        //상수 
        final int C_NUM1;
        C_NUM1 = 10 ;
        System.out.println(C_NUM1);

        final int C_NUM2 = 20;  //상수는 일반적으로 따로 선언하지않고 바로 초기화시킴 / 
        System.out.println(C_NUM2);



    }
    
}
