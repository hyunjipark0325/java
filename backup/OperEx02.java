package backup;
public class OperEx02 {
    public static void main(String[]args){

        //나누기
        int i1 = 3;
        int i2 = 2;
        System.out.println( i1 / i2 ); // 나눈것에 대한 몫만 출력

        //나머지까지 소수점 / 실수로 출력 (실수화를 시켜준후 나누기를해야함)
        System.out.println((double) i1 / i2);
    }
    
}
